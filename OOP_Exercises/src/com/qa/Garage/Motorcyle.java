package com.qa.Garage;

public class Motorcyle extends Vehicle {
	
	private boolean HandlePolish;
	private boolean DoubleSeat;
	
	public Motorcyle() {
		
	}

	public boolean isHandlePolish() {
		return HandlePolish;
	}

	public void setHandlePolish(boolean handlePolish) {
		HandlePolish = handlePolish;
	}

	public boolean isDoubleSeat() {
		return DoubleSeat;
	}

	public void setDoubleSeat(boolean doubleSeat) {
		DoubleSeat = doubleSeat;
	}
	
	
	

}


